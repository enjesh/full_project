print ("Python has three numeric types: int, float, complex")

# Creating a variable
# Introducing integer
myvalue = 1
print (myvalue)
print (type(myvalue))
print (str(myvalue) + " is of the data type " + str(type(myvalue)))

# Introducing float data type
myvalue = 3.14
print (myvalue)
print (type (myvalue))
print (str (myvalue) + " is of the data type " + str(type(myvalue)))

# Introducing complex data type
myvalue = 5j
print (myvalue)
print (type (myvalue))
print (str (myvalue) + " is of the data type " + str (type (myvalue)))


